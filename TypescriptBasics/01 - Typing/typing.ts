// To create a string you use "let" then the varnamen and then the type, so "string"
let myString: string;

// To declare a string it goes as usual
myString = "Yay!";

// Other types are:
let aNumber: number;
let aBoolean: boolean;
let anStringArray: Array<String>;
let anything: any;

// The latter one is better not used, since the whole purpose of typescript is to avoid having these kind of values.
